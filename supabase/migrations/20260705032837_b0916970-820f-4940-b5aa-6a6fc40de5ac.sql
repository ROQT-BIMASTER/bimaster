-- Historico ANTES do update (48 mil linhas esperadas)
INSERT INTO contas_pagar_historico (conta_id, campo_alterado, valor_anterior, valor_novo, usuario_id, justificativa, tipo_alteracao)
SELECT cp.id,
       'plano_contas_id (migracao RES->novo)',
       t_old.code || ' | ' || COALESCE(t_old.name,''),
       t_new.code || ' | ' || COALESCE(t_new.name,''),
       NULL,
       m.justificativa,
       'sistema'
FROM contas_pagar cp
JOIN _res_map m ON m.origem_id = cp.plano_contas_id
JOIN trade_chart_of_accounts t_old ON t_old.id = m.origem_id
JOIN trade_chart_of_accounts t_new ON t_new.id = m.destino_id
WHERE m.destino_id IS NOT NULL;

-- UPDATE em massa das contas a pagar
UPDATE contas_pagar cp
SET plano_contas_id = m.destino_id,
    plano_contas_codigo = t_new.code,
    plano_contas_nome  = t_new.name,
    updated_at = now()
FROM _res_map m
JOIN trade_chart_of_accounts t_new ON t_new.id = m.destino_id
WHERE cp.plano_contas_id = m.origem_id
  AND m.destino_id IS NOT NULL;

-- Regras aprendidas da IA
UPDATE account_classification_rules r
SET plano_contas_id = m.destino_id
FROM _res_map m
WHERE r.plano_contas_id = m.origem_id
  AND m.destino_id IS NOT NULL;

-- Desativar RES-*
UPDATE trade_chart_of_accounts t
SET is_active = false
FROM _res_map m
WHERE t.id = m.origem_id
  AND t.is_active = true;

